class Student {
  final String name;
  final String image;
  final String id;
  final String studentId;
  final String aboutMe;
  final String email;
  final String socialMedia;

  Student({
    required this.name,
    required this.image,
    required this.id,
    required this.studentId,
    required this.aboutMe,
    required this.email,
    required this.socialMedia,
  });
}
